package JavaBasics;

public class dataTypes {
	
   // Datatype-- how we can store our data and which format-
  //primitive data types -- Already defined-- byte, short, char, int, long, float, double, boolean
 // non-primitive datatype --user defined-- String, arrays
	
	
	public static void main(String[] args) {
		//syntax for storing any value-
		// datatype variablename= value;
		
		byte a=65;      //1byte= 8bit    range= -128to127
		System.out.println(a);
		short b=68;
		System.out.println(b);
		int c=567;
		System.out.println(c);
		long d=922337203685477580l;
		System.out.print(d);
		float e=12.7f;
		System.out.println(e);
		double f=18.7889;
		System.out.println(f);
		boolean g=true;
		System.out.println(g);
		char k='p';
		System.out.println(k);
		
		
		
	}

}
